# Задание: № 3.2.8 Дан список случайных чисел. Определить значения минимального и максимального элементов списка.

def find_min_max(arr):
    """
    Алгоритм: однократный проход по списку с обновлением минимума и максимума.
    Сложность: O(N)
    """
    if not arr:
        raise ValueError("Пустой список")

    min_val = arr[0]
    max_val = arr[0]

    for x in arr:
        if x < min_val:
            min_val = x
        if x > max_val:
            max_val = x

    return min_val, max_val


# Пример использования
data = [5, 2, 9, 1, 7]
mn, mx = find_min_max(data)
print("Минимум:", mn)
print("Максимум:", mx)
